let age = 25;
let year = 2023;

console.log(age, year);

age = 30; 
/*  note: since the variable (age) had been previously declared,
    there is no need to use 'let' in asigning it a new value.*/
console.log(age);

const points = 100;
console.log(points);
// const is another method to declare variables except that the values are constant.

var score = 75;
console.log(score);

